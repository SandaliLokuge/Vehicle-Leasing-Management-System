    private void useraddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_useraddButtonActionPerformed
        String UserId = useridText.getText();
        String FirstName = firstnameText.getText();
        String LasttName = lastnameText.getText();
        String NIC = nicText.getText();
        String UserName = usernameText.getText();
        String Password = PasswordField1.getText();

        LeasingOfficer leasingOfficer = new LeasingOfficer(UserId, FirstName, LasttName, NIC, UserName, Password);
        try {
            int res = LeasingOfficerController.addLeasingOfficer(leasingOfficer);
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Leasing Officer Details are successfully added");
            } else {
                JOptionPane.showMessageDialog(this, "Added Failed");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Users.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Users.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_useraddButtonActionPerformed

