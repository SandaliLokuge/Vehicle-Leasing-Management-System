    private void PasswordField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PasswordField2KeyReleased
        if (!PasswordField2.getText().equals(PasswordField1.getText())) {
            errorLabel.setVisible(true);
            correctLabel.setVisible(false);
        }
        if (PasswordField2.getText().equals(PasswordField1.getText())) {
            errorLabel.setVisible(false);
            correctLabel.setVisible(true);
        }
        boolean b1 = firstnameText.getText().equals("") | lastnameText.getText().equals("") | nicText.getText().equals("") | usernameText.getText().equals("") | PasswordField1.getText().equals("") | PasswordField2.getText().equals("");
        boolean b2 = PasswordField2.getText().equals(PasswordField1.getText());
        if (b1 == false & b2 == true) {
            useraddButton.setEnabled(true);
        }
    }//GEN-LAST:event_PasswordField2KeyReleased

