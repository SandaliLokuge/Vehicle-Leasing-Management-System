    private void guarantor1ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guarantor1ButtonActionPerformed
        String GuarantorID = guarantoridText1.getText();
        String NIC = nicText.getText();
        String FirstName = firstnameText.getText();
        String LastName = lastnameText.getText();
        String DateofBirth = dobText.getText();
        String Tel_Home = hometelText.getText();
        String Tel_Office = officetelText.getText();
        String Address_Home = homeaddressText.getText();
        String Address_Office = officeaddressText.getText();
        String Bussiness_Occupation = boText.getText();
        String Gender = null;

        if (maleRadioButton.isSelected()) {
            Gender = "Male";
        } else if (femaleRadioButton.isSelected()) {
            Gender = "Female";
        }

        Guarantor guarantor = new Guarantor(GuarantorID, NIC, FirstName, LastName, DateofBirth, Tel_Home, Tel_Office, Address_Home, Address_Office, Bussiness_Occupation, Gender);

        try {
            int res = GuarantorController.addGuarantor(guarantor);
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Guarantor Details are successfully added");
            } else {
                JOptionPane.showMessageDialog(this, "Added Failed");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Guarantor_details.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Guarantor_details.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            String newGL_Id = GL_IdGenerator.getNewGL_Id("GuarantorsinLeasing", "GL_Id", "GL");

            String GL_Id = newGL_Id;
            String GuarantorId = guarantoridText1.getText();
            String LeaseId = Customer_details.LeaseId;

            GuarantorsinLeasing guarantorsinLeasing = new GuarantorsinLeasing(GL_Id, GuarantorId, LeaseId);
            int res = GuarantorsinLeasingController.addGuarantorstoLease(guarantorsinLeasing);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Guarantor_details.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Guarantor_details.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_guarantor1ButtonActionPerformed

