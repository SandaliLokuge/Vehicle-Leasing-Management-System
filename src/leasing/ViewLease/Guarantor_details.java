    private void GenerateGuarantorId2() {
        String prefix = "G";
        int GuarantorId2 = Integer.parseInt(guarantoridText1.getText().split(prefix)[1]);
        GuarantorId2++;
        NumberFormat nf = NumberFormat.getIntegerInstance();
        nf.setMaximumIntegerDigits(9);
        nf.setGroupingUsed(false);
        String formatnewGuarantorId2 = nf.format(GuarantorId2);
        guarantoridText2.setText(prefix + formatnewGuarantorId2);
    }
