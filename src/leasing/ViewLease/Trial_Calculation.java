    private void processButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processButtonActionPerformed
        double vehiprice = Double.parseDouble(vehiclepriceText.getText());
        double downpayment = Double.parseDouble(downpaymentText.getText());
        double interestrate = Double.parseDouble(interestrateText.getText());
        double leaseterm;
        if (monthCheckBox.isSelected()) {
            leaseterm = Double.parseDouble(termText.getText());
        } else {
            leaseterm = Double.parseDouble(termText.getText()) * 12;
        }
        double leaseamount = vehiprice - downpayment;
        leaseamountText.setText(Double.toString(leaseamount));
        double monthlyinterestpayment;
        monthlyinterestpayment = (leaseamount + (leaseamount * interestrate / 100)) / leaseterm;
        monthlypaymentText.setText(Double.toString(monthlyinterestpayment));

        double totalinterest = leaseamount * interestrate / 100;
        totalinterestText.setText(Double.toString(totalinterest));

        double totalpayment = leaseamount + (leaseamount * interestrate / 100);
        totalpaymenttText.setText(Double.toString(totalpayment));



    }//GEN-LAST:event_processButtonActionPerformed

