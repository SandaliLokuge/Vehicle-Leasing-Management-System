    private void OkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OkButtonActionPerformed

        new Customer_details().setVisible(true);
        this.dispose();

        TC_No = TCText.getText();
        UserId = Login.userid;
        Date = dateLabel.getText();
        LeaseAmount = Double.parseDouble(leaseamountText.getText());

        if (monthCheckBox.isSelected()) {
            No_Installments = Integer.parseInt(termText.getText());
        } else {
            No_Installments = Integer.parseInt(termText.getText()) * 12;
        }

        InterestAmount = Double.parseDouble(interestrateText.getText());
        InstallmentAmount = Double.parseDouble(monthlypaymentText.getText());
        PrepaidAmount = Double.parseDouble(downpaymentText.getText());

        if (monthCheckBox.isSelected()) {
            Period = "Months";
        } else {
            Period = "Years";
        }
        Amount = Double.parseDouble(leaseamountText.getText());


    }//GEN-LAST:event_OkButtonActionPerformed

